public class Distritos {
    private int idDistritos;
    private String nombre;
    private int Zonas_idZonas;

    public int getIdDistritos() {
        return idDistritos;
    }

    public void setIdDistritos(int idDistritos) {
        this.idDistritos = idDistritos;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getZonas_idZonas() {
        return Zonas_idZonas;
    }

    public void setZonas_idZonas(int zonas_idZonas) {
        Zonas_idZonas = zonas_idZonas;
    }
}
